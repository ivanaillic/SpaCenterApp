    private void popuniComboTipNapomene() throws Exception {
        cbTipNapomene.addItem("Odaberite tip napomene za uslugu");

        List<TipNapomeneZaUslugu> listaTipovaNapomena = controller.ClientController.getInstance().getListTipNapomene();
        for (TipNapomeneZaUslugu tipNapomene : listaTipovaNapomena) {
            cbTipNapomene.addItem(tipNapomene);
        }
    }

    private NapomenaZaUslugu kreirajNapomenu() {
        String opisNapomene = txtOpis.getText().trim();
        TipNapomeneZaUslugu tipNapomene = (TipNapomeneZaUslugu) cbTipNapomene.getSelectedItem();

        if (opisNapomene.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti opis napomene!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        NapomenaZaUslugu napomena = new NapomenaZaUslugu();
        napomena.setOpisNapomene(opisNapomene);
        napomena.setTipNapomene(tipNapomene);

        return napomena;
    }

