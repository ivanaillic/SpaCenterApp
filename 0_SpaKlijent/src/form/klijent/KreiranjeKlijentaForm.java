    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        int starost = Integer.parseInt(txtStarost.getText().trim());
        String brojTelefona = txtBrojTelefona.getText().trim();
        String email = txtEmail.getText().trim();

        TipKlijenta tipKlijenta = (TipKlijenta) cbTipKlijenta.getSelectedItem();

        Klijent klijent = new Klijent();
        klijent.setIme(ime);
        klijent.setPrezime(prezime);
        klijent.setStarost(starost);
        klijent.setBrojTelefona(brojTelefona);
        klijent.setEmail(email);
        klijent.setTipKlijenta(tipKlijenta);

        try {
            Klijent k = ClientController.getInstance().addKlijent(klijent);
            JOptionPane.showMessageDialog(this, "Sistem je uspešno zapamtio klijenta!", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti klijenta. Pokušajte ponovo.", "Greška", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(KreiranjeKlijentaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        setLocationRelativeTo(null);
    }//GEN-LAST:event_btnDodajActionPerformed

