  private void popuniComboTipKlijenta() throws Exception {
    cbTipKlijenta.addItem("Odaberite klijenta"); 

    List<TipKlijenta> listaTipovaKlijenta = controller.ClientController.getInstance().getListTipKlijenta();
    for (TipKlijenta tipKlijenta : listaTipovaKlijenta) {
        cbTipKlijenta.addItem(tipKlijenta);
    }
}

