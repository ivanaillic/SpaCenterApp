    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
      String pretraga = txtPretraga.getText().trim().toLowerCase();
    try {
        List<Klijent> listaKlijenata = ClientController.getInstance().getListKlijent();

        String[] pretragaDeliovi = pretraga.split("\\s+");
        List<Klijent> filtriraniKlijenti;

        if (pretragaDeliovi.length == 2) {
            String ime = pretragaDeliovi[0];
            String prezime = pretragaDeliovi[1];

            filtriraniKlijenti = listaKlijenata.stream()
                .filter(k -> k.getIme().toLowerCase().contains(ime) && k.getPrezime().toLowerCase().contains(prezime))
                .toList();
        } else {
            filtriraniKlijenti = listaKlijenata.stream()
                .filter(k -> k.getIme().toLowerCase().contains(pretraga) || k.getPrezime().toLowerCase().contains(pretraga))
                .toList();
        }

        if (!filtriraniKlijenti.isEmpty()) {
            TableModelKlijenti model = new TableModelKlijenti(filtriraniKlijenti);
            tblKlijenti.setModel(model);
            JOptionPane.showMessageDialog(this, "Sistem je pronašao klijente na osnovu unete vrednosti!", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nadje klijente po zadatom kriterijumu", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Sistem ne može da nadje klijente po zadatom kriterijumu ", "Greška", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnPretraziActionPerformed

