    private void btnDetaljiKlijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiKlijentaActionPerformed
        TableModelKlijenti tnm = (TableModelKlijenti) tblKlijenti.getModel();
        int red = tblKlijenti.getSelectedRow();

        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Molim Vas selektujte red!");
        } else {
            int modelRed = tblKlijenti.convertRowIndexToModel(red);
            Klijent k = tnm.vratiNaOsnovuReda(modelRed);
            JOptionPane.showMessageDialog(this, "Sistem je učitao klijenta!", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();

            try {
                new DetaljiKlijenta(k).setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita klijenta!", "Greška", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(PretragaKlijentaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnDetaljiKlijentaActionPerformed

