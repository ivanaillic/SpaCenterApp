    private void popuniComboKlijenata() throws Exception {
        cbKlijenti.addItem("Odaberite klijenta");

        List<Klijent> listaKlijenata = controller.ClientController.getInstance().getListKlijent();
        for (Klijent k : listaKlijenata) {
            cbKlijenti.addItem(k);
        }
    }

    private void popuniComboUsluge() throws Exception {
        cbUsluga.addItem("Odaberite uslugu");

        List<Usluga> listaUsluga = ClientController.getInstance().getListUsluga();
        for (Usluga u : listaUsluga) {
            cbUsluga.addItem(u);
        }
    }

    private void popuniComboVaucera() {
        cbVaucer.addItem("Odaberite vaučer");

        for (Vaucer vaucer : Vaucer.values()) {
            cbVaucer.addItem(vaucer);
        }
    }

