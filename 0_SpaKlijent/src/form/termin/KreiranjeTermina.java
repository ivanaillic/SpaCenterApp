    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        Klijent selectedKlijent = (Klijent) cbKlijenti.getSelectedItem();
        Usluga selectedUsluga = (Usluga) cbUsluga.getSelectedItem();
        int selectedSati = Integer.parseInt(txtSati.getText());
        int selectedMinuti = Integer.parseInt(txtMinuti.getText());
        int selectedBrojProstorije = (int) brojProstorije.getValue();
        String selectedNapomena = txtNapomena.getText();
        Vaucer selectedVaucer = (Vaucer) cbVaucer.getSelectedItem();
        double cenaUsluge = selectedUsluga.getCenaUsluge();
        double konacnaCena = cenaUsluge;

        Calendar calendar = Calendar.getInstance();
        calendar.setTime(dcDatum.getDate());
        calendar.set(Calendar.HOUR_OF_DAY, selectedSati);
        calendar.set(Calendar.MINUTE, selectedMinuti);

        Timestamp selectedDatum = new Timestamp(calendar.getTimeInMillis());

        switch (selectedVaucer) {
            case POPUST10:
                konacnaCena -= cenaUsluge * 0.1;
                break;
            case POPUST20:
                konacnaCena -= cenaUsluge * 0.2;
                break;
            case POKLON_KARTICA:
                konacnaCena = 0;
                break;
            case BEZ_POPUSTA:
                konacnaCena = cenaUsluge;
                break;
        }

        Termin noviTermin = new Termin(selectedKlijent, selectedUsluga, selectedDatum, selectedBrojProstorije, selectedNapomena, selectedVaucer, konacnaCena);
        listaTermina.add(noviTermin);

        TableModelTermini model = new TableModelTermini(listaTermina);
        tblTermini.setModel(model);
        ((AbstractTableModel) tblTermini.getModel()).fireTableDataChanged();
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblTermini.getSelectedRow();
        if (selectedRow != -1) {
            listaTermina.remove(selectedRow);

            TableModelTermini model = new TableModelTermini(listaTermina);
            tblTermini.setModel(model);
            ((AbstractTableModel) tblTermini.getModel()).fireTableDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Molimo vas da prvo selektujete termin koji želite da obrišete.");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            for (Termin termin : listaTermina) {
                ClientController.getInstance().addTermini(termin);
            }
            JOptionPane.showMessageDialog(this, "Sistem je uspešno zapamtio termine.");
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti termine. Pokušajte ponovo!", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed


